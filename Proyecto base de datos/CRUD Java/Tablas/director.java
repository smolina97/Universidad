    private void tabla_directorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_directorMouseClicked
        int fila = tabla_director.getSelectedRow();
        if (fila == -1){
            JOptionPane.showMessageDialog(null,"No ha seleccionado al director a modificar");
        }else{
            id = Integer.parseInt((String)tabla_director.getValueAt(fila, 0).toString());
            String nombre = ((String)tabla_director.getValueAt(fila, 1));
            String apellido = ((String)tabla_director.getValueAt(fila, 2));
            txt_id.setText(""+id);
            txt_nombre.setText(nombre);
            txt_apellido.setText(apellido);
        }
    }//GEN-LAST:event_tabla_directorMouseClicked

