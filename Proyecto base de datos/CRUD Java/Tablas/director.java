    void modificar(){
        String nombre =txt_nombre.getText();
        String apellido =txt_apellido.getText();
        String sql = "update director set nombre ='"+nombre+"',apellido ='"+apellido+"' where id_director="+id;
        if (nombre.equals("")||apellido.equals("")){
            JOptionPane.showMessageDialog(null,"Ingrese Datos");
        }else{
             try {
                cn = con.getConnction();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"Director Actualizado");
                limpiarTabla();
            }catch (Exception e){   
            }     
        }
        
    }
    
    void eliminar(){
        int fila = tabla_director.getSelectedRow();
        if (fila == -1){
            JOptionPane.showMessageDialog(null,"No ha seleccionado al director a modificar");
        }else{
            String sql = "delete from director where id_director ="+id;
            try {
                cn = con.getConnction();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"Director despedido");
                limpiarTabla();
            }catch (Exception e){   
            }     
        }
    }
    
