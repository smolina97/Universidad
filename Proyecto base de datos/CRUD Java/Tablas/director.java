    void listar(){
        String sql = "select * from director";
        try {
            cn = con.getConnction();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            Object[] director = new Object[3];
            modelo = (DefaultTableModel)tabla_director.getModel();
            
            while (rs.next()){
                director[0] = rs.getInt("id_director");
                director[1] = rs.getString("nombre");
                director[2] = rs.getString("apellido");
                modelo.addRow(director);
            }
            tabla_director.setModel(modelo);
        }catch (Exception e){
        }
    }

    void Agregar(){
        String id = txt_id.getText();
        String nombre = txt_nombre.getText();
        String apellido = txt_apellido.getText();
        if (id.equals("")||nombre.equals("")||apellido.equals("")){
            JOptionPane.showMessageDialog(null,"Ingrese Datos");
        }else{
            String sql = "insert into director(id_director,nombre,apellido)values('"+id+"','"+nombre+"','"+apellido+"')";
            try {
                cn = con.getConnction();
                st = cn.createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"Director Agregado");
                limpiarTabla();
            }catch (Exception e){ 
            }     
        }
    }
    void limpiarTabla(){
        for (int i=0 ; i<=tabla_director.getRowCount();i++ ){
            modelo.removeRow(i);
            i=i-1;
        }
    }
    
